class Assignmentoperator
{
    public static void main(String args[])
    {
        int a,b;
        a=8;
        b=9;
        a+=b; // a=a+b
        System.out.println("a="+a);
        a-=b; // a=a-b
        System.out.println("a="+a);
        a*=b; // a=a*b
        System.out.println("a="+a);
        a/=b; // a=a/b
        System.out.println("a="+a);
        a%=b; // a=a%b
        System.out.println("a="+a);
        
    }
}